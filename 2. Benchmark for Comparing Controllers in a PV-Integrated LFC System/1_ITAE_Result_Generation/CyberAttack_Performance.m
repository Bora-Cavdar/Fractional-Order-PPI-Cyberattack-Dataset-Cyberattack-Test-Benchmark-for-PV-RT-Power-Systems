% -------------------------------------------------------------------------
% This script evaluates the cyberattack performance of a PI controller in
% the PV-integrated two-area load frequency control (LFC) system.
%
% The benchmark scenarios are read from:
%   - CyberAttack_Benchmark_PV_RT_Power_System.xlsx
%
% For each scenario, the Simulink model is run under the corresponding:
%   - Random load change condition
%   - False Data Injection (FDI) attack values
%   - Denial-of-Service (DoS) delay values
%
% The maximum ITAE value obtained for each scenario is saved as a
% performance indicator.
%
% Simulink model used in this script:
%   - PV_RT_PI_FDI_DoS_2024a.slx
%
% Required output from the Simulink model:
%   - ITAE
% -------------------------------------------------------------------------

% Load PV-RT LFC system parameters
LFC_PV_Test_System_Parameters;

% -------------------------------------------------------------------------
% User settings
% -------------------------------------------------------------------------
inputFile  = 'CyberAttack_Benchmark_PV_RT_Power_System.xlsx';
outputFile = 'CyberAttack_ITAE_Results_PI.xlsx';

modelName = 'PV_RT_PI_FDI_DoS_2024a';

simulation_time = 15;     % Simulation time in seconds
SAVE_RESULTS = true;      % Set false if you do not want to save Excel output

% -------------------------------------------------------------------------
% PI controller parameters
% -------------------------------------------------------------------------
Kp1 = 0.5663;
Ki1 = 0.4024;

Kp2 = 0.5127;
Ki2 = 0.7256;

% -------------------------------------------------------------------------
% Read benchmark scenario file
% -------------------------------------------------------------------------
data = readtable(inputFile);

% Required columns in the benchmark file
requiredColumns = {'ID', 'SLP2', 'FDI1', 'FDI2', 'DoS1', 'DoS2'};

for c = 1:numel(requiredColumns)
    if ~ismember(requiredColumns{c}, data.Properties.VariableNames)
        error('Required column "%s" was not found in %s.', ...
            requiredColumns{c}, inputFile);
    end
end

% Number of cyberattack benchmark scenarios
num_scenarios = height(data);

% Preallocate result vector
ITAE_max_values = zeros(num_scenarios, 1);

fprintf('Cyberattack benchmark simulation started.\n');
fprintf('Number of scenarios: %d\n\n', num_scenarios);

% -------------------------------------------------------------------------
% Run Simulink model for each benchmark scenario
% -------------------------------------------------------------------------
for i = 1:num_scenarios

    % Assign scenario-specific variables
    SLP2 = data.SLP2(i);
    FDI1 = data.FDI1(i);
    FDI2 = data.FDI2(i);
    DoS1 = data.DoS1(i);
    DoS2 = data.DoS2(i);

    % Clear previous ITAE variable to avoid using old simulation results
    if exist('ITAE', 'var')
        clear ITAE
    end

    try
        % Run the Simulink model
        sim(modelName, simulation_time);

        % Check whether ITAE was generated by the Simulink model
        if ~exist('ITAE', 'var')
            error('The variable "ITAE" was not generated by the Simulink model.');
        end

        % Store the maximum ITAE value for the current scenario
        ITAE_max_values(i) = max(ITAE);

    catch ME
        error('Simulation failed at scenario %d, Scenario ID = %g.\nError message: %s', ...
            i, data.ID(i), ME.message);
    end

    % Display progress
    fprintf('Scenario %d/%d completed. Scenario ID = %g, Max ITAE = %.6f\n', ...
        i, num_scenarios, data.ID(i), ITAE_max_values(i));
end

% -------------------------------------------------------------------------
% Create result table
% -------------------------------------------------------------------------
ITAE_table = table( ...
    data.ID, ...
    data.SLP2, ...
    data.FDI1, ...
    data.FDI2, ...
    data.DoS1, ...
    data.DoS2, ...
    ITAE_max_values, ...
    'VariableNames', { ...
        'Scenario_ID', ...
        'SLP2', ...
        'FDI1', ...
        'FDI2', ...
        'DoS1', ...
        'DoS2', ...
        'Max_ITAE' ...
    } ...
);

